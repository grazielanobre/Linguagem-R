# Controles de Fluxo

# For loop
#se você digitar for e apertar a tecla table ele preenche a estrutura de comando.
for (i in 1:5) {
  print(i) 
}

for (variavel in 1:10) {
  print (variavel + 1)
}

# While loop

variavel <-5
while (variavel > 2) {
variavel <- variavel -1
print (variavel)
}

#IF statement

if (variavel != 2) {
  print( "o valor está correto")
  
}
